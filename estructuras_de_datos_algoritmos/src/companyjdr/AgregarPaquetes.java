    public boolean infoPaquete() {
        //metodo utilizado para que el usuario no guarde o modifique datos sin rellenar los campos necesarios 
        boolean result = false;//se crea un boolean y se iguala a false

        if (destinoNuevoPaqueteTxt.getText().isEmpty() || informacionPaqueteTxt.getText().isEmpty()) {//if que revisa que los textfields de destino e informacion de paquete esten llenos
            JOptionPane.showMessageDialog(null, "Por favor no dejar campos sin rellenar");//si no estan llenos los campos envia una ventana de dialogo JOptionPane que le dira que relene los campos
        } else {
            result = true;//se retorna true 
        }

        return result;//retorna el resultado 
    }

